    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(amarillo.isSelected()){
            jPanel1.setBackground(Color.yellow);
        }else if(azul.isSelected()){
            jPanel1.setBackground(Color.blue);
        }else if(verde.isSelected()){
            jPanel1.setBackground(Color.green);
        }else if(rojo.isSelected()){
            jPanel1.setBackground(Color.red);
        }
        
        // Obtener la fecha de nacimiento desde los campos de texto
        String fechaNacimientoStr = a침o.getText() + "-" + mes.getText() + "-" + dia.getText();

         // Crear un formato de fecha
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");

        try {
        // Parsear la cadena de fecha a un objeto LocalDate
        LocalDate fechaNacimiento = LocalDate.parse(fechaNacimientoStr, formatter);

        // Obtener la fecha actual
        LocalDate fechaActual = LocalDate.now();

        // Calcular la edad en a침os
        int edadEnAnios = fechaActual.getYear() - fechaNacimiento.getYear();

        // Mostrar la edad en el campo de texto 'edad'
        edad.setText(Integer.toString(edadEnAnios));

    } catch (Exception ex) {
        // Manejar la excepci칩n en caso de que la fecha de nacimiento no sea v치lida
        System.out.println("Error al parsear la fecha de nacimiento: " + ex.getMessage());
    }

        
    }//GEN-LAST:event_jButton1ActionPerformed

