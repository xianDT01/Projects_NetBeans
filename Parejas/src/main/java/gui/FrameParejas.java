    private void gestionarGiro(int pos) {
        
          Timer timer = new Timer(100, new ActionListener(){
              
                @Override
                
             public void actionPerformed(ActionEvent e) {
            etiquetas[pos].setIcon(new javax.swing.ImageIcon(getClass().getResource(miBaraja.getRutaImagen(pos))));
        if (miBaraja.getSeleccionada() == 0) {
            miBaraja.setSeleccionada(pos);
            botones[pos].setEnabled(false);
            
        } 
        else {
            
            if (miBaraja.compararPareja(pos, miBaraja.getSeleccionada())) {
                botones[pos].setEnabled(false);
                miBaraja.eliminarPareja(pos, miBaraja.getSeleccionada());
                etiquetas[pos].setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/pareja.jpg")));
                etiquetas[miBaraja.getSeleccionada()].setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/pareja.jpg")));
                miBaraja.setSeleccionada(0);
              
                      
                
            } else {
               
                etiquetas[pos].setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/dorso.jpg")));
                etiquetas[miBaraja.getSeleccionada()].setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/dorso.jpg")));
                botones[miBaraja.getSeleccionada()].setEnabled(true);
                miBaraja.setSeleccionada(0);
                                                
            }
        } 
                }
            });
        timer.setRepeats(false);
        timer.start();


    }

